function normalizeStr(s) {
  return s.normalize('NFD').replace(/[\u0300-\u036f]/g, '').replace(/'/g, '').toLowerCase().trim();
}

function findPlayer(players, nameStr) {
  const normalized = normalizeStr(nameStr);

  for (const p of players) {
    const fullA = normalizeStr(`${p.lastName}, ${p.firstName}`);
    if (fullA === normalized) return p;
  }

  const parts = nameStr.split(',').map(s => s.trim());
  const lastName = parts[0];
  const firstName = parts[1] || '';

  for (const p of players) {
    const lastMatch = normalizeStr(p.lastName) === normalizeStr(lastName);
    const firstMatch = !firstName || normalizeStr(p.firstName).startsWith(normalizeStr(firstName.split(' ')[0]));
    if (lastMatch && firstMatch) return p;
  }

  for (const p of players) {
    if (normalizeStr(p.lastName) === normalizeStr(lastName)) return p;
  }

  return null;
}

async function main() {
  const email = process.argv[2];
  const password = process.argv[3];
  const mode = process.argv[4] || 'validate'; // 'validate' or 'load'
  if (!email || !password) {
    console.log("Uso:");
    console.log("  Validar:  node scripts/load-match-stats.mjs <email> <password>");
    console.log("  Cargar:   node scripts/load-match-stats.mjs <email> <password> load");
    process.exit(1);
  }

  console.log("Autenticando...");
  await signInWithEmailAndPassword(auth, email, password);

  const teamsSnap = await getDocs(collection(db, 'teams'));
  const teams = teamsSnap.docs.map(d => ({ id: d.id, ...d.data() }));

  const playersSnap = await getDocs(collection(db, 'players'));
  const allPlayers = playersSnap.docs.map(d => ({ id: d.id, ...d.data() }));

  let totalOk = 0;
  let totalErrors = 0;
  let totalSkipped = 0;
  const matchesToLoad = [];

  // FASE 1: VALIDACION
  console.log("\n===== VALIDACION =====\n");

  for (const matchInfo of MATCHES) {
    const label = `Fecha ${matchInfo.round}: ${matchInfo.homeTeamName} vs ${matchInfo.awayTeamName}`;

    const homeTeam = teams.find(t => t.name === matchInfo.homeTeamName);
    const awayTeam = teams.find(t => t.name === matchInfo.awayTeamName);

    if (!homeTeam) { console.log(`ERROR  ${label} -> Equipo no encontrado: ${matchInfo.homeTeamName}`); totalErrors++; continue; }
    if (!awayTeam) { console.log(`ERROR  ${label} -> Equipo no encontrado: ${matchInfo.awayTeamName}`); totalErrors++; continue; }

    const matchesSnap = await getDocs(query(collection(db, 'matches'), where('round', '==', matchInfo.round)));
    const match = matchesSnap.docs.find(d => {
      const data = d.data();
      return (data.homeTeamId === homeTeam.id && data.awayTeamId === awayTeam.id) ||
             (data.homeTeamId === awayTeam.id && data.awayTeamId === homeTeam.id);
    });

    if (!match) { console.log(`ERROR  ${label} -> Partido no encontrado en Firestore`); totalErrors++; continue; }

    const matchData = match.data();
    const matchId = match.id;
    const isSwapped = matchData.homeTeamId === awayTeam.id;

    // Determine actual home/away based on match document
    const actualHomeName = isSwapped ? matchInfo.awayTeamName : matchInfo.homeTeamName;
    const actualAwayName = isSwapped ? matchInfo.homeTeamName : matchInfo.awayTeamName;
    const actualHomeTeamId = matchData.homeTeamId;
    const actualAwayTeamId = matchData.awayTeamId;
    const homePointsList = isSwapped ? matchInfo.awayPoints : matchInfo.homePoints;
    const awayPointsList = isSwapped ? matchInfo.homePoints : matchInfo.awayPoints;
    const homePlayers = allPlayers.filter(p => p.teamId === actualHomeTeamId);
    const awayPlayers = allPlayers.filter(p => p.teamId === actualAwayTeamId);

    // Check existing events
    const existingEvents = await getDocs(collection(db, `matches/${matchId}/events`));
    if (existingEvents.size > 0) {
      console.log(`SKIP   ${label} -> Ya tiene ${existingEvents.size} eventos`);
      totalSkipped++;
      continue;
    }

    // Validate scores
    const homeListPts = homePointsList.length;
    const awayListPts = awayPointsList.length;
    const homeDbScore = matchData.homeScore || 0;
    const awayDbScore = matchData.awayScore || 0;

    const homeMatch = homeListPts === homeDbScore;
    const awayMatch = awayListPts === awayDbScore;

    // Validate players exist
    let playerErrors = [];
    for (const name of homePointsList) {
      if (!findPlayer(homePlayers, name)) playerErrors.push(`${name} (${actualHomeName})`);
    }
    for (const name of awayPointsList) {
      if (!findPlayer(awayPlayers, name)) playerErrors.push(`${name} (${actualAwayName})`);
    }

    if (homeMatch && awayMatch && playerErrors.length === 0) {
      console.log(`OK     ${label} -> ${actualHomeName} ${homeListPts}=${homeDbScore} | ${actualAwayName} ${awayListPts}=${awayDbScore}`);
      totalOk++;
      matchesToLoad.push({ matchInfo, matchId, matchData, actualHomeTeamId, actualAwayTeamId, homePointsList, awayPointsList, homePlayers, awayPlayers, actualHomeName, actualAwayName });
    } else {
      let errMsg = `ERROR  ${label} ->`;
      if (!homeMatch) errMsg += ` ${actualHomeName}: lista=${homeListPts} vs db=${homeDbScore}`;
      if (!awayMatch) errMsg += ` ${actualAwayName}: lista=${awayListPts} vs db=${awayDbScore}`;
      if (playerErrors.length > 0) errMsg += ` Jugadores no encontrados: ${playerErrors.join(', ')}`;
      console.log(errMsg);
      totalErrors++;
    }
  }

  console.log(`\n--- Resumen validacion ---`);
  console.log(`OK: ${totalOk} | Errores: ${totalErrors} | Salteados: ${totalSkipped}`);

  // FASE 2: CARGA (solo si mode === 'load')
  if (mode !== 'load') {
    if (totalErrors > 0) {
      console.log(`\nHay ${totalErrors} errores. Corregi los datos antes de cargar.`);
    } else if (matchesToLoad.length > 0) {
      console.log(`\nValidacion OK. Para cargar ejecuta:`);
      console.log(`  node scripts/load-match-stats.mjs ${email} <password> load`);
    } else {
      console.log(`\nNo hay partidos nuevos para cargar.`);
    }
    process.exit(totalErrors > 0 ? 1 : 0);
  }

  if (totalErrors > 0) {
    console.log(`\nHay ${totalErrors} errores. No se cargara nada. Corregi los datos primero.`);
    process.exit(1);
  }

  if (matchesToLoad.length === 0) {
    console.log(`\nNo hay partidos para cargar.`);
    process.exit(0);
  }

  console.log(`\n===== CARGANDO ${matchesToLoad.length} PARTIDOS =====\n`);

  for (const m of matchesToLoad) {
    const label = `Fecha ${m.matchInfo.round}: ${m.actualHomeName} vs ${m.actualAwayName}`;
    let created = 0;

    for (const name of m.homePointsList) {
      const player = findPlayer(m.homePlayers, name);
      await addDoc(collection(db, `matches/${m.matchId}/events`), {
        type: 'ft', made: true, playerId: player.id, teamId: m.actualHomeTeamId, quarter: 1, timestamp: serverTimestamp(),
      });
      created++;
    }

    for (const name of m.awayPointsList) {
      const player = findPlayer(m.awayPlayers, name);
      await addDoc(collection(db, `matches/${m.matchId}/events`), {
        type: 'ft', made: true, playerId: player.id, teamId: m.actualAwayTeamId, quarter: 1, timestamp: serverTimestamp(),
      });
      created++;
    }

    console.log(`OK  ${label} -> ${created} eventos creados`);
  }

  console.log("\nListo! Todos los eventos cargados.");
  process.exit(0);
}

main().catch(err => { console.error(err.message); process.exit(1); });
